#include "libft.h"
char	*ft_strdup(const char *s1)
{
	char	*rez;
	size_t	i;

	i = 0;
	rez = (char *)malloc(ft_strlen(s1) + 1);
	if (rez == 0)
		return (0);
	while (*(s1 + i))
	{
		*(rez + i) = *(s1 + i);
		i++;
	}
	*(rez + i) = '\0';
	return (rez);
}
